#!/usr/bin/env python3
"""Generate paper tables from result JSON.

Reads the real, reproducible outputs and writes LaTeX into paper/. Security and
storage rows are filled from real runs; Fabric per-operation latency is emitted
as TO\\_BE\\_MEASURED until bench_fabric.js is run on a real network.

Run: python3 experiments/gen_tables.py
"""
import json
import os

HERE = os.path.dirname(__file__)
ROOT = os.path.join(HERE, "..")


def load(p):
    with open(os.path.join(ROOT, p)) as f:
        return json.load(f)


def load_optional(p, default):
    """Load a result file if present; otherwise return `default`. Used for the
    Fabric latency results, which do not exist until bench_fabric.js is run on a
    real network -- on a clean checkout the latency rows are TO_BE_MEASURED."""
    path = os.path.join(ROOT, p)
    if not os.path.exists(path):
        return default
    with open(path) as f:
        return json.load(f)


def tex_escape(s):
    return str(s).replace("_", r"\_")


def gen_security():
    data = load("experiments/security_results.json")
    # Editorial mapping: behaviour -> (theorem label or None, short phrase).
    # The DETECTION (checkmark) is the measured part, taken from the real run;
    # the theorem reference and phrase are paper-facing and use \ref so they
    # track theorem renumbering instead of hardcoding "Thm. 1". Keep these
    # labels in sync with \label{thm:...} in main.tex.
    MECH = {
        "drop event within a window": ("thm:within", "root mismatch"),
        "drop an interior window":    ("thm:whole", "commit-time reject"),
        "drop the tail (truncation)": (None, "freshness deadline (stall)"),
        "reorder within a window":    ("thm:reorder", ""),
        "insert a fabricated event":  ("thm:reorder", ""),
        "replay an old window":       ("thm:replay", ""),
    }

    def mechanism(behaviour):
        label, phrase = MECH.get(behaviour, (None, ""))
        if label is None:
            return phrase
        ref = rf"Thm.~\ref{{{label}}}"
        return f"{ref} ({phrase})" if phrase else ref

    lines = [
        r"% AUTO-GENERATED by experiments/gen_tables.py -- do not edit by hand.",
        r"% Requires \usepackage{amssymb} in the preamble (for \checkmark).",
        r"% REPLACES the hand-written tab:adv block in main.tex -- do not keep both.",
        r"\begin{table}[tb]",
        r"\centering",
        r"\caption{Adversarial coordinator vs.\ verifier. The detection column"
        r" is produced by \texttt{run\_security\_battery.py}, a deterministic"
        r" reference simulator, and is reproducible with \texttt{make security};"
        r" it is not a Fabric-timing result.}",
        r"\label{tab:adv}",
        r"\begin{tabular}{lll}",
        r"\toprule",
        r"Adversarial behaviour & Detected & Mechanism \\",
        r"\midrule",
    ]
    for c in data["cases"]:
        if not c["expect_detected"]:
            continue  # honest baseline reported in the footnote, not a row
        det = r"\checkmark" if c["detected"] else r"$\times$"
        behaviour = tex_escape(c["behaviour"])
        behaviour = behaviour[:1].upper() + behaviour[1:]   # sentence case
        lines.append(f"{behaviour} & {det} & {mechanism(c['behaviour'])} \\\\")
    base = next(c for c in data["cases"] if not c["expect_detected"])
    lines += [
        r"\bottomrule",
        r"\end{tabular}",
        r"\par\smallskip",
        r"{\footnotesize Sanity: the honest baseline is \emph{accepted} "
        f"(not flagged), confirming the verifier "
        r"does not reject indiscriminately.}",
        r"\end{table}",
    ]
    with open(os.path.join(ROOT, "paper", "tab_security.tex"), "w") as f:
        f.write("\n".join(lines) + "\n")
    print("wrote paper/tab_security.tex")


def gen_cost():
    storage = load("bench/storage_results.json")
    bench = load_optional("bench/bench_results.json", {"operations": {}})
    s23 = next(r for r in storage["rows"] if r["quorum"] == 2)
    ops = bench.get("operations", {})

    def measured(name):
        o = ops.get(name, {})
        m, p = o.get("median_ms", "TO_BE_MEASURED"), o.get("p95_ms", "TO_BE_MEASURED")
        if m == "TO_BE_MEASURED" or p == "TO_BE_MEASURED":
            return r"\texttt{TO\_BE\_MEASURED}"
        return f"median {tex_escape(m)} ms / p95 {tex_escape(p)} ms"

    lines = [
        r"% AUTO-GENERATED by experiments/gen_tables.py -- do not edit by hand.",
        r"% REPLACES the hand-written tab:cost block in main.tex -- do not keep both.",
        r"\begin{table}[tb]",
        r"\centering",
        r"\caption{Commitment, verification, and storage cost. Complexity and"
        r" the per-anchor world-state size are real and deterministic; storage"
        r" is $O(1)$ per anchor because only the Merkle root, not the leaves, is"
        r" stored. Fabric latencies are \texttt{TO\_BE\_MEASURED} until"
        r" \texttt{bench/bench\_fabric.js} is run on the testbed"
        r" (Sec.~\ref{sec:eval}).}",
        r"\label{tab:cost}",
        r"\begin{tabular}{lll}",
        r"\toprule",
        r"Quantity & Complexity & Measured \\",
        r"\midrule",
        rf"$\mathsf{{CommitWindow}}$ (build root, $N{{=}}100$) & $O(N)$ & {measured('CommitWindow N=100')} \\",
        rf"$\mathsf{{Anchor}}$ validation & $O(1)$ & {measured('Anchor validate')} \\",
        rf"$\mathsf{{Verify}}$ per channel & $O(W_s)$ & {measured('GetAnchors + verify')} \\",
        rf"Anchor world-state size (2-of-3) & $O(1)$/window & {s23['bytesPerAnchor']} bytes (real) \\",
        r"\bottomrule",
        r"\end{tabular}",
        r"\end{table}",
    ]
    with open(os.path.join(ROOT, "paper", "tab_cost.tex"), "w") as f:
        f.write("\n".join(lines) + "\n")
    print("wrote paper/tab_cost.tex")


if __name__ == "__main__":
    gen_security()
    gen_cost()
