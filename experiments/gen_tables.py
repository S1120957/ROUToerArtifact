#!/usr/bin/env python3
"""Generate paper tables from result JSON.

Reads the real, reproducible outputs and writes LaTeX into paper/. Security and
storage rows are filled from real runs; Fabric per-operation latency is emitted
as TO\\_BE\\_MEASURED until bench_fabric.js is run on a real network.

Run: python3 experiments/gen_tables.py
"""
import json
import os

HERE = os.path.dirname(__file__)
ROOT = os.path.join(HERE, "..")


def load(p):
    with open(os.path.join(ROOT, p)) as f:
        return json.load(f)


def load_optional(p, default):
    """Load a result file if present; otherwise return `default`. Used for the
    Fabric latency results, which do not exist until bench_fabric.js is run on a
    real network -- on a clean checkout the latency rows are TO_BE_MEASURED."""
    path = os.path.join(ROOT, p)
    if not os.path.exists(path):
        return default
    with open(path) as f:
        return json.load(f)


def tex_escape(s):
    return str(s).replace("_", r"\_")


def gen_security():
    data = load("experiments/security_results.json")
    lines = [
        r"% AUTO-GENERATED by experiments/gen_tables.py -- do not edit by hand.",
        r"\begin{table}[tb]",
        r"\centering",
        r"\caption{Adversarial coordinator vs.\ verifier. Outcomes are produced"
        r" by \texttt{experiments/run\_security\_battery.py}, a deterministic"
        r" reference simulator; reproducible from a clean checkout with"
        r" \texttt{make security}. Not a Fabric-timing result.}",
        r"\label{tab:adv}",
        r"\begin{tabular}{lll}",
        r"\toprule",
        r"Adversarial behaviour & Detected & Mechanism \\",
        r"\midrule",
    ]
    for c in data["cases"]:
        if not c["expect_detected"]:
            continue  # skip the honest baseline row in the table
        det = r"\checkmark" if c["detected"] else r"$\times$"
        mech = f"{tex_escape(c['maps_to'])} ({tex_escape(c['caught_by'])}: {tex_escape(c['reason'])})"
        lines.append(f"{tex_escape(c['behaviour'])} & {det} & {mech} \\\\")
    # report the honest baseline as a sanity note
    base = next(c for c in data["cases"] if not c["expect_detected"])
    lines += [
        r"\bottomrule",
        r"\end{tabular}",
        r"\par\smallskip",
        r"{\footnotesize Sanity: the honest baseline is \emph{accepted} "
        f"(detected={tex_escape(base['detected'])}), confirming the verifier "
        r"does not reject indiscriminately.}",
        r"\end{table}",
    ]
    with open(os.path.join(ROOT, "paper", "tab_security.tex"), "w") as f:
        f.write("\n".join(lines) + "\n")
    print("wrote paper/tab_security.tex")


def gen_cost():
    storage = load("bench/storage_results.json")
    bench = load_optional("bench/bench_results.json", {"operations": {}})
    # pick the 2-of-3 storage row as the representative
    s23 = next(r for r in storage["rows"] if r["quorum"] == 2)
    ops = bench.get("operations", {})

    def lat(name):
        o = ops.get(name, {})
        return tex_escape(o.get("median_ms", "TO_BE_MEASURED")), \
               tex_escape(o.get("p95_ms", "TO_BE_MEASURED"))

    cw100_m, cw100_p = lat("CommitWindow N=100")
    anc_m, anc_p = lat("Anchor validate")
    ver_m, ver_p = lat("GetAnchors + verify")

    lines = [
        r"% AUTO-GENERATED by experiments/gen_tables.py -- do not edit by hand.",
        r"\begin{table}[tb]",
        r"\centering",
        r"\caption{Per-operation cost. Complexity and per-anchor world-state"
        r" size are real and deterministic; storage is $O(1)$ per anchor"
        r" because only the Merkle root (not the leaves) is stored. Fabric"
        r" per-operation latency is \texttt{TO\_BE\_MEASURED} until"
        r" \texttt{bench\_fabric.js} is run on a real network (Sec.~\ref{sec:eval}).}",
        r"\label{tab:cost}",
        r"\begin{tabular}{llll}",
        r"\toprule",
        r"Quantity & Complexity & Fabric median & Fabric p95 \\",
        r"\midrule",
        rf"\texttt{{CommitWindow}} ($N{{=}}100$) & $O(N)$ & {cw100_m} & {cw100_p} \\",
        rf"\texttt{{Anchor}} validation & $O(1)$ & {anc_m} & {anc_p} \\",
        rf"\texttt{{GetAnchors}}\,+\,verify & $O(W_s)$ & {ver_m} & {ver_p} \\",
        r"\midrule",
        rf"World-state per anchor (2-of-3) & $O(1)$ & "
        rf"\multicolumn{{2}}{{l}}{{{s23['bytesPerAnchor']} bytes (real)}} \\",
        r"\bottomrule",
        r"\end{tabular}",
        r"\end{table}",
    ]
    with open(os.path.join(ROOT, "paper", "tab_cost.tex"), "w") as f:
        f.write("\n".join(lines) + "\n")
    print("wrote paper/tab_cost.tex")


if __name__ == "__main__":
    gen_security()
    gen_cost()
